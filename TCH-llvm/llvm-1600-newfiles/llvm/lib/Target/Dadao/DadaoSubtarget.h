#ifndef LLVM_LIB_TARGET_DADAO_DADAOSUBTARGET_H
#define LLVM_LIB_TARGET_DADAO_DADAOSUBTARGET_H

#include "DadaoFrameLowering.h"
#include "DadaoISelLowering.h"
#include "DadaoInstrInfo.h"
#include "DadaoSelectionDAGInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"

#define GET_SUBTARGETINFO_HEADER
#include "DadaoGenSubtargetInfo.inc"

namespace llvm {

class DadaoSubtarget : public DadaoGenSubtargetInfo {
public:
  // This constructor initializes the data members to match that
  // of the specified triple.
  DadaoSubtarget(const Triple &TargetTriple, StringRef Cpu,
                 StringRef FeatureString, const TargetMachine &TM,
                 const TargetOptions &Options, CodeModel::Model CodeModel,
                 CodeGenOpt::Level OptLevel);

  // ParseSubtargetFeatures - Parses features string setting specified
  // subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  DadaoSubtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS);

  void initSubtargetFeatures(StringRef CPU, StringRef FS);

  bool enableMachineScheduler() const override { return true; }

  const DadaoInstrInfo *getInstrInfo() const override { return &InstrInfo; }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const DadaoRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }

  const DadaoTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  const DadaoSelectionDAGInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

private:
  DadaoFrameLowering FrameLowering;
  DadaoInstrInfo InstrInfo;
  DadaoTargetLowering TLInfo;
  DadaoSelectionDAGInfo TSInfo;
};
} // namespace llvm

#endif // LLVM_LIB_TARGET_DADAO_DADAOSUBTARGET_H
